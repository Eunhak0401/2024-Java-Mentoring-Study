public class week3_5 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println("기본 변수값");
        a++;
        System.out.println("++ 한 이후의 변수값 : " + a);

        int b = 20;
        System.out.println("기본 변수값 : " + b);
        b--;
        System.out.println("--한 이후의 변수값 :" + b);
    }
}
